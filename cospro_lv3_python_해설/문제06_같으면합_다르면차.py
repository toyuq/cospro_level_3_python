"""
=============================================================
COS Pro 3급 - 문제 6: 같으면 합, 다르면 차
=============================================================

[문제 설명]
- 숫자 두 개를 입력받아 같으면 두 수의 합을, 다르면 차를 출력
- 두 숫자는 1 이상 100 이하인 자연수입니다.
- 두번째 숫자는 첫번째 숫자와 같거나 큽니다.

[입출력 예]
  입력: 5, 5   →  출력: 10  (같으므로 5+5)
  입력: 7, 10  →  출력: 3   (다르므로 10-7)

[핵심 개념]
- if-else 조건문
- == 으로 같은지 비교
=============================================================
"""

a = int(input())    # 첫 번째 숫자 입력
b = int(input())    # 두 번째 숫자 입력

if a == b:              # 두 숫자가 같으면
    print(a + b)        # 합 출력
else:                   # 다르면
    print(b - a)        # 차 출력
