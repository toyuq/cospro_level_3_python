"""
=============================================================
COS Pro 3급 - 문제 1: 두 숫자를 더한 값 구하기
=============================================================

[문제 설명]
- 숫자 두 개를 입력받아, 두 수의 합을 출력
- 두 수는 1 이상 100 이하인 자연수입니다.

[입출력 예]
  입력: 5, 3  →  출력: 8
  입력: 1, 4  →  출력: 5

[핵심 개념]
- input()으로 입력 받기
- int()로 숫자로 바꾸기
- + 로 더하기
=============================================================
"""

a = int(input())   # 첫 번째 숫자 입력
b = int(input())   # 두 번째 숫자 입력
print(a + b)       # 두 수의 합 출력
