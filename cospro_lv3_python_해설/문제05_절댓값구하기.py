"""
=============================================================
COS Pro 3급 - 문제 5: 숫자의 절댓값 구하기
=============================================================

[문제 설명]
- 숫자 한 개를 입력받아, 절댓값을 출력
- 표준 입력으로 숫자 한 개가 주어집니다. - 입력되는 수는 -1,000 이상 1,000 이하인 정수입니다.
- 절댓값이란? 부호를 떼고 양수로 만든 값
   예: -13 → 13,  7 → 7

[입출력 예]
  입력: -13  →  출력: 13
  입력: 7    →  출력: 7

[핵심 개념]
- if문으로 음수인지 확인
- 음수이면 -1을 곱해서 양수로 바꾸기
=============================================================
"""

a = int(input())    # 숫자 입력

if a < 0:           # 만약 음수이면
    a = a * -1      # -1을 곱해서 양수로 바꿈

print(a)            # 결과 출력
