"""
=============================================================
COS Pro 3급 - 문제 4: 문자열 양 옆에 큰따옴표 붙이기
=============================================================

[문제 설명]
- 문자열 한 개를 입력받아, 양 옆에 큰따옴표를 붙여 출력
- 문자열은 알파벳 대문자와 소문자로만 이루어집니다.
- 문자열 길이는 1 이상 100 이하입니다.

[입출력 예]
  입력: HelloWorld   →  출력: "HelloWorld"
  입력: Quote        →  출력: "Quote"

[핵심 개념]
- 문자열 연결 (+)
- 큰따옴표를 출력하려면 \" 사용
=============================================================
"""

a = input()                     # 문자열 입력
print("\"" + a + "\"")          # 앞뒤에 큰따옴표를 붙여서 출력
