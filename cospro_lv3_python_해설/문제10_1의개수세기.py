"""
=============================================================
COS Pro 3급 - 문제 10: 문자열에서 '1'의 개수 구하기
=============================================================

[문제 설명]
- 문자열을 입력받아 그 안에 '1'이 몇 개 있는지 출력
- 문자열 s의 길이는 1 이상 1,000 이하입니다.
- 문자열 s는 '1'~'9'로 이루어진 문자열입니다.

[입출력 예]
  입력: "4156721"  →  출력: 2
  입력: "48273"    →  출력: 0

[핵심 개념]
- for문으로 문자열을 한 글자씩 확인
- if문으로 '1'인지 비교
- 개수를 세는 변수 활용
=============================================================
"""

s = input()         # 문자열 입력
count = 0           # '1'의 개수를 세는 변수 (0부터 시작)

# 문자열을 처음부터 끝까지 한 글자씩 확인
for i in range(len(s)):
    if s[i] == '1':         # 이 글자가 '1'이면
        count = count + 1   # 개수를 1 증가

print(count)        # 최종 개수 출력
