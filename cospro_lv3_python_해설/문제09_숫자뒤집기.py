"""
=============================================================
COS Pro 3급 - 문제 9: 숫자를 거꾸로 출력
=============================================================

[문제 설명]
- 자연수를 입력받아 거꾸로 출력
  예: 12345 → 54321
- n은 1 이상 100,000,000 이하입니다.
- n은 0으로 끝나지 않습니다.

[입출력 예]
  입력: 12345  →  출력: 54321

[핵심 개념]
- 문자열을 한 글자씩 접근: n[i]
- for문으로 뒤에서부터 앞으로 반복
- 빈 문자열에 글자를 하나씩 이어붙이기 (+)
=============================================================
"""

n = input()     # 문자열로 입력받음 (글자 하나씩 다루기 위해)

# 빈 문자열을 만들고, 뒤에서부터 한 글자씩 이어붙이기
# 예: n = "12345" → 길이는 5
#   i=4: n[4] = '5' → result = "5"
#   i=3: n[3] = '4' → result = "54"
#   i=2: n[2] = '3' → result = "543"
#   i=1: n[1] = '2' → result = "5432"
#   i=0: n[0] = '1' → result = "54321"
result = ""
for i in range(len(n) - 1, -1, -1):
    result = result + n[i]

print(result)
