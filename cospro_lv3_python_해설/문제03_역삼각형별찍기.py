"""
=============================================================
COS Pro 3급 - 문제 3: 별로 역 직각삼각형 출력
=============================================================

[문제 설명]
- 자연수 N을 입력받아 별(*)로 거꾸로 된 삼각형을 출력
- N은 1 이상 100 이하입니다.

[입출력 예]
  입력: 5
  출력:
    *****
    ****
    ***
    **
    *

[핵심 개념]
- for 반복문
- '*' * 숫자 → 별을 숫자만큼 반복

[range 개념]
- range(끝)           → 0부터 끝-1까지, 1씩 증가
- range(시작, 끝)      → 시작부터 끝-1까지, 1씩 증가
- range(시작, 끝, 증감) → 시작부터 끝 직전까지, 증감만큼 변화
=============================================================
"""

n = int(input())    # 숫자 입력

# n부터 1까지 줄어들면서 반복
# 예: n=5이면 i는 5, 4, 3, 2, 1
for i in range(n, 0, -1):
    print('*' * i)      # 별을 i개 출력
