"""
=============================================================
COS Pro 3급 - 문제 2: 두 문자열 중 더 긴 문자열 찾기
=============================================================

[문제 설명]
- 문자열 두 개를 입력받아, 더 긴 문자열을 출력

[입출력 예]
  입력: "HelloWorld", "Programmingisfun"
  출력: "Programmingisfun"

[핵심 개념]
- len()으로 글자 수 세기
- if-else로 비교하기
=============================================================
"""

s1 = input()    # 첫 번째 문자열 입력
s2 = input()    # 두 번째 문자열 입력

# 글자 수를 각각 세서 변수에 저장
a = len(s1)
b = len(s2)

# 더 긴 쪽을 출력
if a > b:
    print(s1)
else:
    print(s2)
