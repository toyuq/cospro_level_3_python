"""
=============================================================
COS Pro 3급 - 문제 8: 배열에서 인접 원소의 차 출력
=============================================================

[문제 설명]
- 숫자 n개를 입력받아 배열을 만들고, 앞 숫자에서 뒷 숫자를 뺀 값을 차례로 출력
- n은 2 이상 50 이하입니다.
- arr의 원소는 -100 이상 100 이하인 정수입니다.


[입출력 예]
  입력: 4개 숫자 → 1, 3, 6, 2
  출력:
    -2   (1 - 3)
    -3   (3 - 6)
     4   (6 - 2)

[핵심 개념]
- 빈 리스트 만들기 + append로 추가
- for문으로 입력 여러 번 받기
- 배열[i]로 원소 접근
=============================================================
"""

n = int(input())    # 배열의 길이 입력

# 숫자를 하나씩 입력받아 배열 만들기
arr = []                        # 빈 배열 만들기
for i in range(n):              # n번 반복
    x = int(input())            # 숫자 하나 입력
    arr.append(x)               # 배열 끝에 추가

# 앞 숫자 - 뒷 숫자를 차례로 출력
# 예: [1, 3, 6, 2]
#   arr[0] - arr[1] = 1 - 3 = -2
#   arr[1] - arr[2] = 3 - 6 = -3
#   arr[2] - arr[3] = 6 - 2 =  4
for i in range(n - 1):
    print(arr[i] - arr[i + 1])
