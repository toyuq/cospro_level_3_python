"""
=============================================================
COS Pro 3급 - 문제 7: 1부터 n까지 숫자 출력
=============================================================

[문제 설명]
- 자연수 n을 입력받아 1부터 n까지 숫자를 출력
- 표준 입력으로 자연수 n이 주어집니다. n은 1 이상 1,000 이하입니다.

[입출력 예]
  입력: 5
  출력:
    1
    2
    3
    4
    5

[핵심 개념]
- for 반복문
- range(1, n+1) → 1부터 n까지
=============================================================
"""

n = int(input())    # 숫자 입력

# 1부터 n까지 하나씩 출력
# range(1, n+1) → 1, 2, 3, ..., n
for i in range(1, n + 1):
    print(i)
